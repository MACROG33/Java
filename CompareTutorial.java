class CompareTutorial {
    public static void main(String[] args) {
        // true /false
        
        int a=10,b=5;
        
        //ตัวดำเนินการ การเปรียบเทียบ
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        //ตัวดำเนินการทางตรรกศาสตร์
        
        

    }
}
