class Prepost {
  public static void main(String[] args) {
    //pre หน้า | post หลัง
    int a=5;
    System.out.println(a);
    System.out.println(a++);
    System.out.println(a);

    int b=5;
    System.out.println(b);
    System.out.println(--b);
    System.out.println(b);
  }  
}
